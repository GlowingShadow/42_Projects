#include "ft_ls.h"

void	ft_clean(char ***tab, int i)
{
	int	j;

	j = ft_tablen(*tab);
	while (i < j)
	{
		if ((*tab)[i][ft_strlen((*tab)[i]) - 1] == '/')
			(*tab)[i][ft_strlen((*tab)[i]) - 1] = 0;
		i++;
	}
}
