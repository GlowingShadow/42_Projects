#include "push_swap.h"

void	ft_putnbr_str(int *n)
{
	int	i;

	i = 1;
	while (i < n[0])
	{
		ft_putnbr(n[i]);
		if (i != (n[0] - 1))
			ft_putchar(' ');
		i++;
	}
}
