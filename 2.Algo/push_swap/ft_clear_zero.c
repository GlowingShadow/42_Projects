#include "push_swap.h"

void	ft_clear_zero(char ***av)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	while ((*av)[i] != NULL)
	{
		j = 0;
		if ((*av)[i][j] == '-')
			j++;
		k = j;
		while ((*av)[i][j] == '0')
			j++;
		if ((*av)[i][j] == '\0')
		{
			j--;
			k = 0;	
		}
		ft_strcpy((*av)[i] + k, (*av)[i] + j);
		i++;
	}
}
